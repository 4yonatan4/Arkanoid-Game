package levelreader;

/**
 * The type Space.
 */
public class Space {

    private String symbol;

    /**
     * Gets symbol.
     *
     * @return the symbol
     */
    public String getSymbol() {
        return symbol;
    }

    /**
     * Sets symbol.
     *
     * @param newSymbol the symbol
     */
    public void setSymbol(String newSymbol) {
        this.symbol = newSymbol;
    }

    /**
     * Gets width.
     *
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * Sets width.
     *
     * @param newWidth the width
     */
    public void setWidth(int newWidth) {
        this.width = newWidth;
    }

    private int width;

}
